    void consultar(){
        String sql = "select * from ingredientes";
        try{
            conet = con1.getConnection();
            st = conet.createStatement();
            rs=st.executeQuery(sql);
            Object[] ingrediente = new Object[3];
            modelo = (DefaultTableModel)inventario.getModel();
            while (rs.next()){
                ingrediente[0] = rs.getInt("id");
                ingrediente[1] = rs.getString("nombre");
                ingrediente[2] = rs.getInt("cantidad");
                modelo.addRow(ingrediente);
                inventario.setModel(modelo);
            }
            
        }catch(Exception e){
            
        }
    }
    
    void Agregar(){
        String nom= Aingrediente.getText();
        String cant = Acantidad.getText();
        try{
            if(nom.equals("")||cant.equals("")){
                JOptionPane.showMessageDialog(null, "Ingrese todos los datos");
                limpiarTabla();
            }else{
                String sql="insert into ingredientes(nombre,cantidad)VALUES ('"+nom+"','"+cant+"')";
                conet = con1.getConnection();
                st = conet.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Nuevo Ingrediente Agregado.");
                Aingrediente.setText("");
                Acantidad.setText("");
                limpiarTabla();
                
            }
        }catch(Exception e){
            
        }
    }
    
    void limpiarTabla(){
        for (int i=0; i <= inventario.getRowCount(); i++){
            modelo.removeRow(i);
            i=i-1;
        }
    }
    
    void eliminar(){
        int fila = inventario.getSelectedRow();
        try{
            if(fila<0){
                JOptionPane.showMessageDialog(null, "Seleccione ingrediente a eliminar");
                limpiarTabla();
            }else{
                String sql="delete from ingredientes where id="+idc;
                conet = con1.getConnection();
                st = conet.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Ingrediente eliminado.");
                Aingrediente.setText("");
                Acantidad.setText("");
                limpiarTabla();
            }
        }catch(Exception e){
            
        }
    }

