    void modificar(){
        String nom= Aingrediente.getText();
        String cant = Acantidad.getText();
        try{
            if(nom.equals("")||cant.equals("")){
                JOptionPane.showMessageDialog(null, "Seleccione el ingrediente a modificar");
                limpiarTabla();
            }else{
                String sql="update ingredientes set nombre='"+nom+"',cantidad='"+cant+"' where id='"+idc+"' ";
                conet = con1.getConnection();
                st = conet.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Ingrediente Actualizado.");
                Aingrediente.setText("");
                Acantidad.setText("");
                limpiarTabla();
            }
        }catch(Exception e){
            
        }
    }
    
